# Đây là ghi chú một dòng

"""
Đây là ghi chú nhiều dòng.
Có thể dùng để mô tả chức năng hoặc giải thích đoạn mã.
"""

def tinh_tong(a, b):
    """
    Hàm này trả về tổng của hai số a và b.
    Đây là docstring (chuỗi tài liệu) cho hàm.
    """
    return a + b

# In ra kết quả
print(tinh_tong(2, 3))  # Ghi chú cuối dòng