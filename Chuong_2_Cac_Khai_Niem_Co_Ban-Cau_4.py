# Kiểu số nguyên
so_nguyen = 10
print("Kiểu int:", so_nguyen, type(so_nguyen))

# Kiểu số thực
so_thuc = 3.14
print("Kiểu float:", so_thuc, type(so_thuc))

# Kiểu logic
gia_tri_logic = True
print("Kiểu bool:", gia_tri_logic, type(gia_tri_logic))

# Kiểu chuỗi
chuoi = "Xin chào Python"
print("Kiểu str:", chuoi, type(chuoi))

# Kiểu danh sách
danh_sach = [1, 2, 3]
print("Kiểu list:", danh_sach, type(danh_sach))

# Kiểu bộ
bo = (1, 2, 3)
print("Kiểu tuple:", bo, type(bo))

# Kiểu từ điển
tu_dien = {"a": 1, "b": 2}
print("Kiểu dict:", tu_dien, type(tu_dien))

# Kiểu tập hợp
tap_hop = {1, 2, 3}
print("Kiểu set:", tap_hop, type(tap_hop))