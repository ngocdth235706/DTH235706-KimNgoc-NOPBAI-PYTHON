a = 10
b = 3

# Toán tử chia lấy phần thập phân
print("a / b =", a / b)  # Kết quả: 3.333...

# Toán tử chia lấy phần nguyên
print("a // b =", a // b)  # Kết quả: 3

# Toán tử lấy phần dư
print("a % b =", a % b)  # Kết quả: 1

# Toán tử lũy thừa
print("a ** b =", a ** b)  # Kết quả: 1000

# Toán tử logic and
x = True
y = False
print("x and y =", x and y)  # Kết quả: False

# Toán tử logic or
print("x or y =", x or y)  # Kết quả: True

# Toán tử kiểm tra đối tượng (is)
m = [1, 2]
n = m
k = [1, 2]
print("m is n =", m is n)  # Kết quả: True (cùng đối tượng)
print("m is k =", m is k)  # Kết quả: False (khác đối